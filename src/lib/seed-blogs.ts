// Function to seed blogs to Firestore
export async function seedBlogs() {
    console.log("Starting blog seeding...");

    for (const blog of seoBlogs) {
        try {
            const existingBlog = await dbService.getBlogBySlug(blog.slug);
            if (!existingBlog) {
                await dbService.createBlog(blog);
                console.log(`✅ Created blog: ${blog.title}`);
            } else {
                console.log(`⏭️  Blog already exists: ${blog.title}`);
            }
        } catch (error) {
            console.error(`❌ Error creating blog ${blog.title}:`, error);
        }
    }

    console.log("Blog seeding complete!");
}
